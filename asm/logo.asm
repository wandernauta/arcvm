! logo.asm
! Draws a logo to the arcvm display device.

	.begin
	.org 2048
	sethi 0x3fffc0, %r1	! set r1 to the IO offset

	V_COLOR .equ 0xF1
	V_CMD .equ 0xF2
	V_ADDR_X .equ 0xF4
	V_ADDR_Y .equ 0xF8

	SCREENW .equ 320
	SCREENH .equ 240
	IMAGE_W .equ 100	! In pixels
	IMAGE_H .equ 44		! In pixels
	
init:	
	ld [white], %r4		! %r4 = the color (white)
	or %r0, 10, %r2		! %r2 = the x coord
	or %r0, 10, %r3		! %r3 = the y coord
	or %r0, 0, %r9		! %r9 = data offset
nexty:
	subcc %r3, IMAGE_H, %r0
	be done

	inc %r3
	or %r0, 0, %r2
loop:
	subcc %r2, IMAGE_W, %r0
	be nexty

	ld [%r9 + logo], %r6
	st %r2, [%r1+V_ADDR_X]
	st %r3, [%r1+V_ADDR_Y]
	st %r6, [%r1+V_COLOR]
	add %r2, 4, %r2
	add %r9, 4, %r9		! Increase data offset

	ba loop
done:
	st %r6, [%r1+V_CMD]
	halt
